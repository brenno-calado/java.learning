class ModDemo {
    public static void main(String[] args) {
        int integerResult = 10 / 3, integerRemainder = 10 % 3;
        double doubleResult = 10.0 / 3.0, doubleRemainder = 10.0 % 3;

        System.out.println("10 / 3");
        System.out.println("integerResult: " + integerResult + " integerRemainder: " + integerRemainder);
        System.out.println("doubleResult: " + doubleResult + " doubleRemainder: " + doubleRemainder);
    }
}