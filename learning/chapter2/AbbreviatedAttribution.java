package chapter2;
public class AbbreviatedAttribution {
    public static void main(String[] args) {
        int x = 10;

        x -= 1;
        System.out.println(x);

        x += 2;
        System.out.println(x);

        x *= 2;
        System.out.println(x);

        x /= 3;
        System.out.println(x);

        x %= 3;
        System.out.println(x);

        x &= 1;
        System.out.println(x);

        x |= 1;
        System.out.println(x);

        x ^= 0;
        System.out.println(x);
    }
}
