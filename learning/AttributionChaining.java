class AttributionChaining {
    public static void main(String[] args) {
        int x, y, z;
        x = y = z = 100;

        System.out.println("x, y and z were attributed at the same time, with the same value 100");
        System.out.println(x);
        System.out.println(y);
        System.out.println(z);
    }
}